package shopping;

public class ProductType {
    String jeans;
    String shirt;
    String color;
    String brand;
    String size;

    public ProductType(String jeans, String shirt, String color, String brand, String size) {
        this.jeans = jeans;
        this.shirt = shirt;
        this.color = color;
        this.brand = brand;
        this.size = size;
    }
}
